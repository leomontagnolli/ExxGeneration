package com.genera.veiculos;

public class Carro extends Veiculo {
	
	private String cor;
	private int NmrPortas;
	private int veloMax;
	
	
	public String getCor() {
		return cor;
	}
	public void setCor(String cor) {
		this.cor = cor;
	}
	public int getNmrPortas() {
		return NmrPortas;
	}
	public void setNmrPortas(int nmrPortas) {
		NmrPortas = nmrPortas;
	}
	public int getVeloMax() {
		return veloMax;
	}
	public void setVeloMax(int veloMax) {
		this.veloMax = veloMax;
	}

	
	

}
